            datos.setFont(FontFactory.getFont("Arial", 16, Font.BOLDITALIC, BaseColor.BLACK));

            Paragraph tituloCompra = new Paragraph("Detalles de la compra del cliente\n\n", FontFactory.getFont("Arial", 16, Font.BOLDITALIC, BaseColor.ORANGE));
