            detalleCompra.add("IVA ya incluida\n");
            detalleCompra.add("Total a pagar: " + (String.valueOf((Integer.parseInt(Cantidad.getText())) * (Integer.parseInt(Precio.getText())))) + ".00 Pesos\n");
            detalleCompra.add("Gracias por su compra\n");
