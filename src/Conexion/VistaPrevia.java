    void mostrarPrevia() {
        int filas = tbPDF.getSelectedRow();
        if (filas > 0) {
            Folio.setText(tbPDF.getValueAt(filas, 0).toString());
            clavePro.setText(tbPDF.getValueAt(filas, 1).toString());
            Nombre.setText(tbPDF.getValueAt(filas, 2).toString());
            Direccion.setText(tbPDF.getValueAt(filas, 3).toString());
            Cantidad.setText(tbPDF.getValueAt(filas, 4).toString());
            Descripcion.setText(tbPDF.getValueAt(filas, 5).toString());
            Precio.setText(tbPDF.getValueAt(filas, 6).toString());

        }
    }

    void mostrarDatos() {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("Folio");
        modelo.addColumn("Clave_Pro");
        modelo.addColumn("Nombre del Cliente");
        modelo.addColumn("Direccion");
        modelo.addColumn("Cantidad");
        modelo.addColumn("Descripcion");
        modelo.addColumn("Precio");
        tbPDF.setModel(modelo);
        String sql = "";
        if (sql.equals("")) {
            sql = "select fa.FOLIO,pro.CLAVE_P, c.NOMBRE_C,c.DIRECCION_C,"
                    + " df.CANTIDAD, pro.DESCRIPCION,pro.PRECIO"
                    + " from clientes c inner join factura fa "
                    + "on fa.CLAVE_C=c.CLAVE_C  "
                    + "inner join detallefactura df on fa.FOLIO=df.FOLIO "
                    + "inner join productos pro on pro.CLAVE_P=df.CLAVE_P;";
        }
        String[] datos = new String[7];
        try {
            Statement st = cn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            while (rs.next()) {
                datos[0] = rs.getString(1);
                datos[1] = rs.getString(2);
                datos[2] = rs.getString(3);
                datos[3] = rs.getString(4);
                datos[4] = rs.getString(5);
                datos[5] = rs.getString(6);
                datos[6] = rs.getString(7);

                modelo.addRow(datos);
            }
            tbPDF.setModel(modelo);
        } catch (SQLException ex) {
            System.out.println("error " + ex);
        }
    }

