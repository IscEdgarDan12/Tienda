            table.addCell(new Paragraph("Folio", FontFactory.getFont("Arial", 12, Font.BOLDITALIC, BaseColor.ORANGE)));
            table.addCell(new Paragraph("Clave_Pro", FontFactory.getFont("Arial", 12, Font.BOLDITALIC, BaseColor.ORANGE)));
            table.addCell(new Paragraph("Nombre del Cliente", FontFactory.getFont("Arial", 12, Font.BOLDITALIC, BaseColor.ORANGE)));
            table.addCell(new Paragraph("Direccion", FontFactory.getFont("Arial", 12, Font.BOLDITALIC, BaseColor.ORANGE)));
            table.addCell(new Paragraph("Cantidad", FontFactory.getFont("Arial", 12, Font.BOLDITALIC, BaseColor.ORANGE)));
            table.addCell(new Paragraph("Descripcion", FontFactory.getFont("Arial", 12, Font.BOLDITALIC, BaseColor.ORANGE)));
            table.addCell(new Paragraph("Precio", FontFactory.getFont("Arial", 12, Font.BOLDITALIC, BaseColor.ORANGE)));
